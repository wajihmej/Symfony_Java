    public function findProduct($Value, $order)
    {
        $em = $this->getEntityManager();
        if ($order == 'DESC') {
            $query = $em->createQuery(
                'SELECT r FROM App\Entity\Produit r   where r.nom like :var OR r.rate like :var OR r.prix like :var  order by r.prix DESC '
            );
            $query->setParameter('var', $Value . '%');
        } else {
            $query = $em->createQuery(
                'SELECT r FROM App\Entity\Produit r   where r.nom like :var OR r.rate like :var OR r.prix like :var  order by r.prix ASC '
            );
            $query->setParameter('var', $Value . '%');
        }
        return $query->getResult();

    }

